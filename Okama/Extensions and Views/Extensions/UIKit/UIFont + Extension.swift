import UIKit

extension UIFont {
	static let avenir20: UIFont? = {
		return UIFont(name: "avenir", size: 20)
	}()
	static let avenir26: UIFont? = {
		return UIFont(name: "avenir", size: 26)
	}()
	static let laoSangamMN20: UIFont? = {
		return UIFont(name: "Lao Sangam MN", size: 20)
	}()
	static let laoSangamMN18: UIFont? = {
		return UIFont(name: "Lao Sangam MN", size: 18)
	}()
}
